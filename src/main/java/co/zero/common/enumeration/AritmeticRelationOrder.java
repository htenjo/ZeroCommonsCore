package co.zero.common.enumeration;

/**
 * Enumeración que reune las operaciones aritméticas de orden
 * @author Hernán Tenjo
 * @version 1.0
 */
public enum AritmeticRelationOrder {
	//Operación menor que (<)
	LESS_THAN,
	//Operación menor o igual que (<=)
	LESS_EQUAL_THAN,
	//Operación igual que (==)
	EQUAL_THAN,
	//Operación mayor o igual que (>=)
	GREATER_EQUAL_THAN,
	//Operación mayor que (>)
	GREATER_THAN
}